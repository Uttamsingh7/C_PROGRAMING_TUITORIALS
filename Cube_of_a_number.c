/* Description: C program to print Cube of a number. */

#include <stdio.h>
int main ()

{
	int x;
	printf ("Enter the number:");
	scanf ("%d", &x);
	printf ("Cube of %d is= %d\n", x, x*x*x);
	return 0;
}

